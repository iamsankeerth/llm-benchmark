"""Promptfoo quality evaluation module."""

from __future__ import annotations

import json
import os
import shutil
import subprocess
from datetime import datetime
from pathlib import Path
from typing import Any, Callable

from src.artifact_store import BenchmarkArtifactStore
from src.lifecycle import delete_ollama_tag, pull_ollama_tag
from src.model_entry import as_model_entry, safe_name

LogFn = Callable[[str], None]
TestCatalog = Callable[[], list[dict[str, Any]]]


class QualityEvaluator:
    def __init__(
        self,
        model_queue: list[dict[str, Any]],
        promptfoo_dir: str | Path,
        results_dir: str | Path,
        test_catalog: TestCatalog,
    ):
        self.model_queue = model_queue
        self.promptfoo_dir = Path(promptfoo_dir)
        results_path = Path(results_dir)
        self.store = BenchmarkArtifactStore(results_path.parent if results_path.name == "results" else None)
        self.store.results_dir = results_path
        self.store.quality_dir = results_path / "quality"
        self.quality_dir = self.store.quality_dir
        self.config_path = self.promptfoo_dir / "promptfooconfig.yaml"
        self.test_catalog = test_catalog
        self.promptfoo_dir.mkdir(parents=True, exist_ok=True)
        self.quality_dir.mkdir(parents=True, exist_ok=True)

    @staticmethod
    def find_promptfoo() -> str:
        found = shutil.which("promptfoo")
        if found:
            return found
        npm_global = os.path.join(os.environ.get("APPDATA", ""), "npm", "promptfoo.cmd")
        if os.path.isfile(npm_global):
            return npm_global
        npx = shutil.which("npx")
        if npx:
            return npx
        raise FileNotFoundError("promptfoo not found. Install with: npm install -g promptfoo")

    def get_ollama_providers(self) -> list[dict[str, Any]]:
        providers = []
        seen = set()
        for model in self.model_queue:
            entry = as_model_entry(model)
            if not entry.is_runnable:
                continue
            if not entry.is_runtime("ollama"):
                continue
            tag = entry.ollama_tag or entry.resolved_model_ref
            if not tag or tag in seen:
                continue
            seen.add(tag)
            providers.append(
                {
                    "id": f"ollama:chat:{tag}",
                    "label": entry.requested_name,
                    "queue_id": entry.queue_id,
                    "config": {"temperature": 0.7, "num_predict": 512},
                }
            )
        return providers

    def generate_config(self, log: LogFn | None = None) -> bool:
        providers = self.get_ollama_providers()
        if not providers:
            _log(log, "ERROR: No Ollama providers found in MODEL_QUEUE")
            return False
        tests = self.test_catalog()
        grader_model = (
            "qwen2.5:3b-instruct"
            if any("qwen2.5:3b" in p["id"] for p in providers)
            else providers[0]["id"].replace("ollama:chat:", "")
        )
        self.config_path.write_text(
            self.render_config(providers, tests, grader_model),
            encoding="utf-8",
        )
        _log(log, f"Generated {self.config_path}")
        _log(log, f"  {len(providers)} Ollama providers")
        _log(log, f"  {len(tests)} curated test cases")
        _log(log, f"  Grading model: {grader_model}")
        return True

    def generate_single_model_config(self, ollama_tag: str, label: str, grader_tag: str) -> str:
        config_path = self.promptfoo_dir / f"config_{safe_name(ollama_tag)}.yaml"
        config_path.write_text(
            self.render_config(
                [{"id": f"ollama:chat:{ollama_tag}", "label": label, "config": {"temperature": 0.7, "num_predict": 512}}],
                self.test_catalog(),
                grader_tag,
                description=f"Quality eval: {label}",
                header=f"# Single-model config for {label}",
            ),
            encoding="utf-8",
        )
        return str(config_path)

    @staticmethod
    def render_config(
        providers: list[dict[str, Any]],
        tests: list[dict[str, Any]],
        grader_tag: str,
        description: str = "LLM Benchmark Suite - Quality Evaluation",
        header: str | None = None,
    ) -> str:
        lines = [
            header or f"# Auto-generated by quality_eval.py on {datetime.now().isoformat()}",
            f"# {len(providers)} Ollama models x {len(tests)} curated tests",
            "",
            f'description: "{description}"',
            "",
            "providers:",
        ]
        for provider in providers:
            lines.append(f"  - id: {provider['id']}")
            lines.append(f"    label: \"{provider['label']}\"")
            lines.append("    config:")
            lines.append(f"      temperature: {provider['config']['temperature']}")
            lines.append(f"      num_predict: {provider['config']['num_predict']}")

        lines.extend(
            [
                "",
                "prompts:",
                "  - \"{{prompt}}\"",
                "",
                "defaultTest:",
                "  options:",
                "    provider:",
                "      text:",
                f"        id: ollama:chat:{grader_tag}",
                "        config:",
                "          temperature: 0.1",
                "          num_predict: 1024",
                "    maxConcurrency: 1",
                "    timeout: 120000",
                "",
                "tests:",
            ]
        )
        for test in tests:
            lines.append(f"  - description: \"{_yaml_str(test['description'])}\"")
            lines.append("    vars:")
            lines.append(f"      prompt: \"{_yaml_str(test['vars']['prompt'])}\"")
            lines.append("    assert:")
            for assertion in test["assert"]:
                lines.append(f"      - type: {assertion['type']}")
                if "value" in assertion:
                    lines.append(f"        value: \"{_yaml_str(assertion['value'])}\"")
                if "threshold" in assertion:
                    lines.append(f"        threshold: {assertion['threshold']}")
        return "\n".join(lines) + "\n"

    def run_eval(self, log: LogFn | None = None) -> bool:
        providers = self.get_ollama_providers()
        if not providers:
            _log(log, "ERROR: No Ollama providers found in MODEL_QUEUE")
            return False

        tests = self.test_catalog()
        grader_tag = "qwen2.5:3b-instruct"
        skip_lifecycle = os.environ.get("BENCHMARK_SKIP_LIFECYCLE") == "1"

        _log(log, "=" * 60)
        _log(log, "promptfoo Quality Evaluation")
        _log(log, "Lifecycle: Download -> Eval -> Delete (one model at a time)")
        _log(log, f"{len(providers)} models x {len(tests)} tests")
        _log(log, f"Grading model: {grader_tag}")
        _log(log, "=" * 60)
        _log(log, "\n[0] Downloading grading model...")
        pull_ollama_tag(grader_tag, log=log, timeout=600)

        all_model_results: dict[str, Any] = {}
        for idx, provider in enumerate(providers, 1):
            tag = provider["id"].replace("ollama:chat:", "")
            label = provider["label"]
            queue_id = provider.get("queue_id", "")
            target_model = os.environ.get("BENCHMARK_SINGLE_MODEL")
            if target_model and not _matches_target_model(target_model, tag, label, queue_id):
                continue

            _log(log, f"\n[{idx}/{len(providers)}] {label} ({tag})")
            result_file = self.store.quality_result_path(tag)
            if result_file.is_file():
                _log(log, "  Already evaluated, skipping")
                try:
                    all_model_results[tag] = json.loads(result_file.read_text(encoding="utf-8"))
                except Exception:
                    pass
                continue

            if tag != grader_tag and not skip_lifecycle:
                if not pull_ollama_tag(tag, log=log, timeout=600):
                    _log(log, "  Skipping (download failed)")
                    continue
            elif skip_lifecycle:
                _log(log, f"  [Orchestrator] Skipping download, assuming {tag} is ready.")

            config_path = ""
            try:
                config_path = self.generate_single_model_config(tag, label, grader_tag)
                output_file = self.store.quality_raw_result_path(tag)
                promptfoo_bin = self.find_promptfoo()
                use_npx = promptfoo_bin.endswith("npx.exe") or promptfoo_bin.endswith("npx")
                cmd = ([promptfoo_bin] if not use_npx else [promptfoo_bin, "promptfoo"]) + [
                    "eval",
                    "-c",
                    config_path,
                    "--no-cache",
                    "--max-concurrency",
                    "1",
                    "-o",
                    str(output_file),
                    "--no-progress-bar",
                ]

                _log(log, f"  Running {len(tests)} tests...")
                result = subprocess.run(
                    cmd,
                    cwd=self.promptfoo_dir,
                    capture_output=True,
                    encoding="utf-8",
                    errors="replace",
                    timeout=1800,
                )
                if result.returncode in (0, 100) and output_file.is_file():
                    summary = self.summarize_raw_result(tag, label, output_file)
                    result_file.write_text(json.dumps(summary, indent=2), encoding="utf-8")
                    all_model_results[tag] = summary
                    _log(log, f"  {summary['passed']}/{summary['total_tests']} passed ({summary['pass_rate']}%)")
                else:
                    _log(log, f"  Eval failed (exit {result.returncode})")
                    if result.stderr:
                        _log(log, f"  stderr: {result.stderr.strip()[-300:]}")
            except subprocess.TimeoutExpired:
                _log(log, "  Eval timed out (30 min)")
            except FileNotFoundError:
                _log(log, "  'promptfoo' command not found - install with: npm install -g promptfoo")
            except Exception as exc:
                _log(log, f"  Unexpected error: {exc}")
            finally:
                if config_path:
                    try:
                        Path(config_path).unlink()
                    except Exception:
                        pass
                if tag != grader_tag and not skip_lifecycle:
                    delete_ollama_tag(tag, log=log)

        if not skip_lifecycle:
            delete_ollama_tag(grader_tag, log=log)

        summary_file = self.quality_dir / "promptfoo_summary.json"
        summary_file.write_text(
            json.dumps(
                {
                    "timestamp": datetime.now().isoformat(),
                    "total_models": len(all_model_results),
                    "results": all_model_results,
                },
                indent=2,
            ),
            encoding="utf-8",
        )
        self._log_summary_table(all_model_results, log)
        return True

    @staticmethod
    def summarize_raw_result(tag: str, label: str, output_file: str | Path) -> dict[str, Any]:
        raw = json.loads(Path(output_file).read_text(encoding="utf-8"))
        results_list = raw.get("results", {}).get("results", [])
        passed = sum(1 for result in results_list if result.get("success"))
        total = len(results_list)
        return {
            "model": tag,
            "label": label,
            "total_tests": total,
            "passed": passed,
            "failed": total - passed,
            "pass_rate": round(passed / total * 100, 1) if total else 0,
        }

    def _log_summary_table(self, all_model_results: dict[str, Any], log: LogFn | None) -> None:
        _log(log, "\n" + "=" * 60)
        _log(log, f"{'Model':<30} {'Pass':>6} {'Total':>6} {'Rate':>8}")
        _log(log, "-" * 60)
        for _, result in sorted(
            all_model_results.items(),
            key=lambda item: item[1].get("pass_rate", 0),
            reverse=True,
        ):
            name = result.get("label", result.get("model", ""))[:28]
            _log(log, f"  {name:<28} {result['passed']:>6} {result['total_tests']:>6} {result['pass_rate']:>7.1f}%")
        _log(log, "=" * 60)
        _log(log, f"Results saved to: {self.quality_dir}")


def _yaml_str(value: Any) -> str:
    return str(value).replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")


def _matches_target_model(target_model: str, tag: str, label: str, queue_id: str) -> bool:
    target = target_model.lower()
    return (
        target_model == queue_id
        or target == queue_id.lower()
        or target == tag.lower()
        or target in tag.lower()
        or target in label.lower()
    )


def _log(log: LogFn | None, message: str) -> None:
    if log:
        log(message)
